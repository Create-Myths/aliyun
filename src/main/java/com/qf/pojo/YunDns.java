package com.qf.pojo;

public class YunDns {
    private Long dnsid;

    private Long hid;

    private String domain;

    public Long getDnsid() {
        return dnsid;
    }

    public void setDnsid(Long dnsid) {
        this.dnsid = dnsid;
    }

    public Long getHid() {
        return hid;
    }

    public void setHid(Long hid) {
        this.hid = hid;
    }

    public String getDomain() {
        return domain;
    }

    public void setDomain(String domain) {
        this.domain = domain;
    }
}